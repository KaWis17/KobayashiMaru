    @Override
    public int evaluate() {

        if(!Config.PIECE_SQUARE_TABLE_ON)
            return 0;

        int value = 0;

        for(byte i = 1; i <= 64; i++) {
            switch (board.getPieceOnSquare(i)) {
                case (WHITE | PAWN) -> value += whitePawns[i-1];
                case (WHITE | KNIGHT) -> value += whiteKnights[i-1];
                case (WHITE | BISHOP) -> value += whiteBishops[i-1];
                case (WHITE | ROOK) -> value += whiteRooks[i-1];
                case (WHITE | QUEEN) -> value += whiteQueens[i-1];
                case (WHITE | KING) -> value += whiteKing[i-1];
                case (BLACK | PAWN) -> value -= blackPawns[i-1];
                case (BLACK | KNIGHT) -> value -= blackKnights[i-1];
                case (BLACK | BISHOP) -> value -= blackBishops[i-1];
                case (BLACK | ROOK) -> value -= blackRooks[i-1];
                case (BLACK | QUEEN) -> value -= blackQueens[i-1];
                case (BLACK | KING) -> value -= blackKing[i-1];
            }
        }

       return value*2;
    }
